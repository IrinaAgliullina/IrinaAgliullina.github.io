var c = {color1: "red", color2: "white"};
console.log("var c = {color1: red, color2: white}, тип данных: " + typeof c); // object
console.log("\n");

var d = null;
console.log("var d = null, тип данных: " + typeof d); //  object
console.log("\n");

var e = true;
console.log("var e = true, тип данных: " + typeof e); // boolean
console.log("\n");

